/*
 * This program demonstrate the usage of the various type of the comments
 * Single or multiple line comments
 */
class CommentExample{
  public static void main(String [] args){
        int a = 10; // Initializing variable a with value 10 
        int b = 20; // Initializing variable b with value 20

        /*declaring c variable to hold addition of a + b variable*/

        int c;

        c = a + b;

        System.out.println("c + "+c);

    }
}