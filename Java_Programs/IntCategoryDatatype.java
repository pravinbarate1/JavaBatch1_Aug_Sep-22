class IntCategoryDatatype{
    public static void main(String [] args){
        byte b = 127;
        short s = 32767;
        int i = 231212;
        long l = 342342;

        System.out.println("b = "+b);
        System.out.println("s = "+s);
        System.out.println("i = "+i);
        System.out.println("l = "+l);
    }
}